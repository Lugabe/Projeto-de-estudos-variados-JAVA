package Controle;

public class For3 {

	public static void main(String[] args) {
		
		for(int i =0; i <10; i++) {
			for(int j =0; j <10; j++) {
				System.out.printf("%d %d", i,j); 
				// para usar a variavel fora do bloco precisa declarar fora do bloco
				
			}
			System.out.println();
		}
	}
}
